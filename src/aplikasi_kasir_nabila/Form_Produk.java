     private void ambilData(){
     try {
            PreparedStatement s = con.prepareStatement("SELECT * FROM tbl_satuan");
            ResultSet r = s.executeQuery();
            while (r.next()) {                
                cbsat.addItem(r.getString("nama_satuan"));
            }
            
            PreparedStatement sm = con.prepareStatement("SELECT * FROM tbl_kategori");
            ResultSet rm = sm.executeQuery();
            while (rm.next()) {                
                cbkat.addItem(rm.getString("nama_kategori"));
            }
        }catch(Exception e){
            
        }
}
     
     private void refreshTable(){
        tm = new DefaultTableModel(
            null,
            new Object[] {"Kode Produk","Nama Produk","Harga Jual","Harga Beli","Stok","Satuan","Kategori"}
        );
        tbl_datapro.setModel(tm);
        tm.getDataVector().removeAllElements();
        try {
            PreparedStatement s = con.prepareStatement("SELECT * FROM tbl_produk");
            ResultSet r = s.executeQuery();
            while(r.next()) {
                Object[] data = {
                 r.getString(1),
                 r.getString(2),
                 r.getString(3),
                 r.getString(4),
                 r.getString(5),
                 r.getString(6),
                 r.getString(7)
                };
                tm.addRow(data);
            }
        } catch(Exception e) {
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
}

    
