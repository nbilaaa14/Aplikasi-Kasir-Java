    private void tbl_dataproMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_dataproMouseClicked
        // TODO add your handling code here:
        int i = tbl_datapro.getSelectedRow();
        if (i == -1) {
            return;
        }
        String kode_produk = (String) tm.getValueAt (i, 0);
        txtkode_pro.setText(kode_produk);
        String nama_produk = (String) tm.getValueAt (i, 1);
        txtnm_pro.setText(nama_produk);
        String harga_jual = (String) tm.getValueAt (i, 2);
        txtharga_jual.setText(harga_jual);
        String harga_beli = (String) tm.getValueAt (i, 3);
        txtharga_beli.setText(harga_beli);
        String stok = (String) tm.getValueAt (i, 4);
        txtstok.setText(stok);
        String nama_satuan = (String) tm.getValueAt (i, 5);
        cbsat.setSelectedItem(nama_satuan);
        String nama_kategori = (String) tm.getValueAt (i, 6);
        cbkat.setSelectedItem(nama_kategori );
    }//GEN-LAST:event_tbl_dataproMouseClicked

